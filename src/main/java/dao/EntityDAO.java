package dao;

import entity.UserEntity;

import java.util.List;
import java.util.Optional;

/**
 * Интерфейс DAO (Data Access Object) для управления сущностями {@link UserEntity}.
 * <p>
 * Предоставляет стандартные CRUD-операции для взаимодействия с постоянным хранилищем данных.
 */
public interface EntityDAO {

    /**
     * Сохраняет новую сущность пользователя.
     * <p>
     * При успешном выполнении возвращает объект с присвоенным уникальным идентификатором.
     *
     * @param userEntity объект пользователя для сохранения; не должен быть {@code null}.
     * @return {@link Optional}, содержащий сохраненную сущность, или пустой {@link Optional},
     * если операция была прервана.
     */
    Optional<UserEntity> create(UserEntity userEntity);

    /**
     * Выполняет поиск пользователя по его уникальному идентификатору.
     *
     * @param id уникальный идентификатор пользователя.
     * @return {@link Optional} с найденным пользователем, или пустой {@link Optional},
     * если запись с таким идентификатором отсутствует в базе данных.
     */
    Optional<UserEntity> read(Long id);

    /**
     * Обновляет состояние существующей сущности пользователя.
     * <p>
     * Метод синхронизирует изменения переданного объекта с записью в базе данных.
     *
     * @param userEntity объект с обновленными данными; поле ID должно быть заполнено
     * и соответствовать существующей записи.
     * @return {@link Optional}, содержащий обновленное состояние сущности,
     * или пустой {@link Optional}, если обновление не удалось.
     */
    Optional<UserEntity> update(UserEntity userEntity);

    /**
     * Удаляет запись пользователя по его идентификатору.
     *
     * @param id уникальный идентификатор пользователя, подлежащего удалению.
     * @return {@link Optional}, содержащий данные удаленного пользователя,
     * или пустой {@link Optional}, если пользователь не был найден.
     */
    Optional<UserEntity> remove(Long id);

    /**
     * Извлекает список всех пользователей из БД.
     *
     * @return список всех объектов {@link UserEntity}. Если данных нет,
     * возвращается пустой список (никогда не возвращает {@code null}).
     */
    List<UserEntity> findAll();
}
